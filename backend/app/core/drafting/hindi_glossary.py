def apply_hindi_terms(text: str) -> str:
    """Replace English legal terms with Hindi equivalents in text.

    Performs case-insensitive replacement of known legal terms.
    Only replaces complete phrases (not partial word matches).

    Args:
        text: English legal text to transform.

    Returns:
        Text with legal terms replaced by Hindi equivalents.
    """
    import re

    result = text
    # Sort by length descending to replace longer phrases first
    sorted_terms = sorted(
        LEGAL_GLOSSARY.items(), key=lambda x: len(x[0]), reverse=True
    )
    for english, hindi in sorted_terms:
        # Word-boundary replacement (case-insensitive)
        pattern = re.compile(re.escape(english), re.IGNORECASE)
        result = pattern.sub(hindi, result)
    return result


def get_hindi_term(english_term: str) -> str | None:
    """Look up a single Hindi translation for an English legal term.

    Case-insensitive lookup.
    """
    lower = english_term.strip().lower()
    for key, value in LEGAL_GLOSSARY.items():
        if key.lower() == lower:
            return value
    return None


